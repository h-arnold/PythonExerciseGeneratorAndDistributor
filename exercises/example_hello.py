"""
Example Exercise: Hello World

This is a simple example exercise to demonstrate the structure.
Complete the function below to return a greeting message.
"""


def greet(name: str) -> str:
    """
    Return a greeting message for the given name.

    Args:
        name: The name to greet

    Returns:
        A greeting message

    Example:
        >>> greet("World")
        'Hello, World!'
    """
    return f"Hello, {name}!"
